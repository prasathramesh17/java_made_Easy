package section6_datatype_typecast_var;

// VARIABLE-- variables are containers for storing data values.
          //syntax -- { type variableName = value;

//datatypes--[primitive datatype] (int,float,double,boolean,float,long,char,byte,short)
//       [non-primitive datatype]  (string ,array...etc)
public class Variable_Assignment {

	public static void main(String[] args) {

		int num = 10;
		float val = 20.54f;
		char c = 'x';
		String quote = "I am the best";
		
		System.out.println(num);
		System.out.println(val);
		System.out.println(c);
		System.out.println(quote);
	}

}
