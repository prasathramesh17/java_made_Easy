package Section9_flow_control_statements;

public class Do_while {
//atleast once the body of the loop will be executed even condition is false
	//after the statement execution it will look the while condition
	public static void main(String[] args) {

		int x =1;
		
		do {
			System.out.println("inside the loop");
		}while(x<1);
	}

}
