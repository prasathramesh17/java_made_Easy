package Section9_flow_control_statements;

public class While_Demo {

	public static void main(String[] args) {

		int i =1;
		
		while(i<=10) {
			System.out.println(i);
			i++;     //to increment by 1
			         //  to increment by more num compound increment can be used
			          //Ex: i+=2;  (i=i+2) increase by 2
		}
	}

}

